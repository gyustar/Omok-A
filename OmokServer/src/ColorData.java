class ColorData {
    private int player0Color;
    private int player1Color;

    ColorData(int player0Color, int player1Color) {
        this.player0Color = player0Color;
        this.player1Color = player1Color;
    }

    int getPlayer0Color() {
        return player0Color;
    }

    int getPlayer1Color() {
        return player1Color;
    }
}