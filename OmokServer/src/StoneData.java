public class StoneData {
    private int i;
    private int j;
    private int color;

    public StoneData(int i, int j, int color) {
        this.i = i;
        this.j = j;
        this.color = color;
    }

    int getI() {
        return i;
    }

    int getJ() {
        return j;
    }

    int getColor() {
        return color;
    }
}